f=input('DIGITE ALGO:')
print('O tipo primitivo desse valor é', type(f))
print('É um número?',f.isnumeric())
print('Só tem espaço?', f.isspace())
print('É alfabético?', f.isalpha())
print('É alfanumérico?', f.isalnum())
print('Está em maiúscula?',f.isupper())
print('Está em minúscula?', f.islower())
print('Está capitalizada?', f.istitle())
